def string_upper(input_string: str):
    """
    Возвращает результат применения
    метода upper к аргументу типа str
    """
    ret_val = input_string.upper()
    return ret_val


def string_title(input_string: str):
    """
    Применяет метод title к аргументу
    функции типа str
    """
    ret_val = input_string.title()
    return ret_val